namespace TheNote\core\command;

use pocketmine\event\Listener;
use pocketmine\player\Player;
use TheNote\core\BaseAPI;
use TheNote\core\Main;
use pocketmine\command\Command;
use pocketmine\command\CommandSender;
use pocketmine\utils\Config;

class MyMoneyCommand extends Command implements Listener
{
    public function __construct(Main $plugin)
    {
        $this->plugin = $plugin;
        $api = new BaseAPI();
        parent::__construct("mymoney", $api->getSetting("prefix") . $api->getLang("mymoneyprefix"), "/mymoney");
    }

    public function execute(CommandSender $sender, string $commandLabel, array $args): bool
    {
        $api = new BaseAPI();
        if (!$sender instanceof Player) {
            $sender->sendMessage($api->getSetting("error") . $api->getLang("commandingame"));
            return false;
        }
        $api = new BaseAPI();
        $money = $api->getMoney($sender->getName());
        $message = str_replace("{money}", $money, $api->getLang("mymoney"));
        $sender->sendMessage($api->getSetting("money") . $message);
        return true;
    }
}