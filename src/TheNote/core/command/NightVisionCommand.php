namespace TheNote\core\command;

use pocketmine\entity\effect\EffectInstance;
use pocketmine\entity\effect\VanillaEffects;
use pocketmine\player\Player;
use pocketmine\utils\Config;
use TheNote\core\BaseAPI;
use TheNote\core\Main;
use pocketmine\command\Command;
use pocketmine\command\CommandSender;

class NightVisionCommand extends Command
{

    public function __construct(Main $plugin)
    {
        $this->plugin = $plugin;
        $api = new BaseAPI();
        parent::__construct("nightvision", $api->getSetting("prefix") . $api->getLang("nightvprefix"), "/nightvision", ["nachtsicht", "ns", "nv"]);
        $this->setPermission("core.command.nightvision");
    }

    public function execute(CommandSender $sender, string $commandLabel, array $args): bool
    {
        $api = new BaseAPI();
        if (!$sender instanceof Player) {
            $sender->sendMessage($api->getSetting("error") . $api->getLang("commandingame"));
            return false;
        }
        if (!$this->testPermission($sender)) {
            $sender->sendMessage($api->getSetting("error") . $api->getLang("nopermission"));
            return false;
        }
        $sender->sendMessage($api->getSetting("info") . $api->getLang("nightvsucces"));
        $sender->getEffects()->add(new EffectInstance(VanillaEffects::NIGHT_VISION(), (12000), (1), false));
        return false;
    }
}


