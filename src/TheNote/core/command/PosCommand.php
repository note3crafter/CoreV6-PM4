namespace TheNote\core\command;

use pocketmine\player\Player;
use pocketmine\utils\Config;
use TheNote\core\BaseAPI;
use TheNote\core\Main;
use pocketmine\command\Command;
use pocketmine\command\CommandSender;

class PosCommand extends Command
{

    public function __construct(Main $plugin)
    {
        $this->plugin = $plugin;
        $api = new BaseAPI();
        parent::__construct("position", $api->getSetting("prefix") . $api->getLang("posprefix"), "/position", ["pos"]);
        $this->setPermission("core.command.position");
    }

    public function execute(CommandSender $sender, string $commandLabel, array $args): bool
    {
        $api = new BaseAPI();
        if (!$sender instanceof Player) {
            $sender->sendMessage($api->getSetting("error") . $api->getLang("commandingame"));
            return false;
        }
        if (!$this->testPermission($sender)) {
            $sender->sendMessage($api->getSetting("error") . $api->getLang("nopermission"));
            return false;
        }
        $message = str_replace("{x}", $sender->getLocation()->getX(), $api->getLang("possucces"));
        $message1 = str_replace("{y}", $sender->getLocation()->getY() , $message);
        $message2 = str_replace("{z}", $sender->getLocation()->getZ() , $message1);
        $sender->sendMessage($api->getSetting("prefix") . $message2);
        return true;
    }
}